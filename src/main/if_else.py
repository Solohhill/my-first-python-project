val1 = input("What is the first value?")
val2 = input("What is the second value?")
operation = input("What do you want to do next?")
if "+" == operation:
    print(int(val1) + int(val2))
if "-" == operation:
    print(int(val1) - int(val2))

if "%" == operation:
    print(int(val1) % int(val2))







